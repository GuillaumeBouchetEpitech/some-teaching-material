






my_boolean = False # True/False

print(f"my_boolean  --------->  '{type(my_boolean)}'") # bool



my_number = 123 # whole number

print(f"my_number  --------->  '{type(my_number)}'") # int



my_decimal = 0.9999 # decimal number

print(f"my_decimal  --------->  '{type(my_decimal)}'") # float



my_string = "Hello <3" # -> string -> "chain of character" -> "string of character" -> string

print(f"my_string  --------->  '{type(my_string)}'") # str



my_list = ["String", 777, 99.999, True] # -> list of items

print(f"my_list  --------->  '{type(my_list)}'") # list



my_dict = { "String": 777 } # -> dict of items

print(f"my_dict  --------->  '{type(my_dict)}'") # dict

print(f"my_dict['String']  --------->  '{type(my_dict['String'])}'") # int (777)




